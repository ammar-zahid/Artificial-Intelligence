string = "python"

print(len(string)) #to find length of string

print(string.endswith("oon")) #checks if string ends with OON

print(string.count("y")) #checks how many Y are there in string

print(string.capitalize()) #capitalize first word

print(string.upper()) #convert to upper case

print(string.find("t")) #finds the index of character

print(string.replace("o", "n")) #replace o with n